<div id="review-body" class="review-body clearfix">

<p>The latest entry-level chipset from MediaTek is here with the Helio G36. For most intents and purposes it’s a rebranded Helio G37 with a 100Mhz clock reduction on its Cortex-A53 cores which now operate at 2.2GHz instead of 2.3GHz. The rest of the provided specs show no difference between the old chip and the newly released one.</p>

<img class="inline-image" width="1200" height="653" src="https://fdn.gsmarena.com/imgroot/news/23/02/mediatek-helio-g36-ofic/-1200/gsmarena_00.jpg" alt="MediaTek Helio G36 announced - G37 with 100MHz lower CPU clock">

<p>The MediaTek Helio G36 is expected to debut on a budget gaming phone set to launch in India soon. Last year’s Helio G37 debuted on the Moto G22 but there’s no indication which maker will debut the newer Helio G36.</p>

